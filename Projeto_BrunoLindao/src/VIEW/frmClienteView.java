    public void preenche_busca(){
        try{
            ClienteDAO objClienteDAO = new ClienteDAO();
            ClienteDTO objClienteDTO = new ClienteDTO();
            
            ResultSet rsClienteDAO = objClienteDAO.ListaBusca(objClienteDTO);
            
            String[] todos = {"Id", "Nome", "Endereco", "Cidade", "Estado", "Telefone"};
            String[][] linhas = {};
            DefaultTableModel tableModel = new DefaultTableModel(linhas, todos){
                public boolean isCellEditable(int row, int col){
                    return false;
                }
            };
            
            Vector<Vector> dados = new Vector<>();
            while(rsClienteDAO.next()){
                Vector regVetor = new Vector();
                regVetor.add(rsClienteDAO.getInt("id_cliente"));
                regVetor.add(rsClienteDAO.getString("nome_cliente"));
                regVetor.add(rsClienteDAO.getString("endereco_cliente"));
                regVetor.add(rsClienteDAO.getString("estado_cliente"));
                regVetor.add(rsClienteDAO.getString("cidade_cliente"));
                regVetor.add(rsClienteDAO.getString("telefone_cliente"));
                
                dados.add(regVetor);
                tableModel.addRow(regVetor);
            }
            tblCliente.setModel(tableModel);
        }
        catch (SQLException e){
            System.err.println(e.getMessage());
        }
    }
    
    public void setarCampos(){
        int setar = tblCliente.getSelectedRow();
        txtId.setText(tblCliente.getModel().getValueAt(setar, 0).toString());
        txtNome.setText(tblCliente.getModel().getValueAt(setar, 1).toString());
        txtEndereco.setText(tblCliente.getModel().getValueAt(setar, 2).toString());
        txtCidade.setText(tblCliente.getModel().getValueAt(setar, 3).toString());
        txtEstado.setText(tblCliente.getModel().getValueAt(setar, 4).toString());
        txtTelefone.setText(tblCliente.getModel().getValueAt(setar, 5).toString());
    }
    
    public void AlterarCampos(){
        String nome, endereco, cidade, estado, telefone;
        int id;
        
        nome = txtNome.getText();
        endereco = txtEndereco.getText();
        id = Integer.parseInt(txtId.getText());
        telefone=txtTelefone.getText();
        cidade=txtCidade.getText();
        estado=txtEstado.getText();
        
        ClienteDTO objClienteDTO = new ClienteDTO();
        objClienteDTO.setCidade(cidade);
        objClienteDTO.setEndereco(endereco);
        objClienteDTO.setEstado(estado);
        objClienteDTO.setNome(nome);
        objClienteDTO.setTelefone(telefone);
        objClienteDTO.setId(id);
        
        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.AlterarCliente(objClienteDTO);
    }
    
    public void ExcluirCampos(){
        int id;
        
        id = Integer.parseInt(txtId.getText());
        
        ClienteDTO objClienteDTO = new ClienteDTO();
        objClienteDTO.setId(id);
        
        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.ExcluirCliente(objClienteDTO);
    }
